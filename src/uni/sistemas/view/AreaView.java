    private void procesar(int i) {
        switch (i) {
            case 1:
                a.setInput(Double.parseDouble(txtInput.getText()));
                a.setAltura(Double.parseDouble(txtAltura.getText()));
                a.setBase(Double.parseDouble(txtBase.getText()));
                ac.procesar(a);
                txtResultado.setText(String.valueOf(a.getOut()));
                break;
            case 2:
                int x,
                 y;
                r = new Random();
                x = r.nextInt(100);
                y = r.nextInt(100);
                txtAltura.setText(String.valueOf(x));
                txtBase.setText(String.valueOf(y));
                txtAltura.setText(String.valueOf(y));
                txtBase.setText(String.valueOf(x));
                txtInput.setText("");
                txtResultado.setText("");
                txtAltura.requestFocus();
                break;

        }

    }
