    private class starArea implements ActionListener {

        @Override
        public void actionPerformed(ActionEvent ae) {
            if (s < 30) {
                s++;
                txtContador.setText("" + s);

            } else if (s == 30) {
                if (txtInput.getText().length() > 0) {
                    
                    try {
                        procesar(1);
                        Thread.sleep(5000);
                        s = 0;
                        procesar(2);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(AreaView.class.getName()).log(Level.SEVERE, null, ex);
                    }

                } else if (txtInput.getText().length() == 0) {
                    a.setRespuesta((a.getBase()*a.getAltura())/2);
                    JOptionPane.showMessageDialog(rootPane, "Ingrese Respuesta a verificar\n"
                            + "El resultado es: "+a.getRespuesta());
                    s = 0;
                    procesar(2);
                }
            }
        }

    }

